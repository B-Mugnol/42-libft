#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!new)
		return ;
	if (!lst)
		*lst = ft_lstnew((*new).content);
	else
	{
		new -> next = *lst;
		*lst = new;
	}
}
